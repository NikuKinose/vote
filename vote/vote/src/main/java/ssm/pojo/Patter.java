package ssm.pojo;

public class Patter {
    private Integer patterId;

    private String patterName;

    private String patterSetExplain;

    public Integer getPatterId() {
        return patterId;
    }

    public void setPatterId(Integer patterId) {
        this.patterId = patterId;
    }

    public String getPatterName() {
        return patterName;
    }

    public void setPatterName(String patterName) {
        this.patterName = patterName;
    }

    public String getPatterSetExplain() {
        return patterSetExplain;
    }

    public void setPatterSetExplain(String patterSetExplain) {
        this.patterSetExplain = patterSetExplain;
    }
}